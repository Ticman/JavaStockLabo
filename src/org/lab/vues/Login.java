    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Dao dao = new Dao();
        //Initialisation de deux variable
        String user = IHM_nomutilisateur.getText();
        String mdp = IHM_motdepasse.getText();
        
        Query req = dao.em().createQuery("SELECT u FROM Utilisateur u WHERE u.nomut=:nomut AND u.motpasse=:motpasse");
  req.setParameter("nomut",user);
  req.setParameter("motpasse",mdp);
  try{
      utilisateur = (Utilisateur) req.getSingleResult();
        if (user.equals(utilisateur.getNomut()) && mdp.equals(utilisateur.getMotpasse())){
       this.dispose();
       Accueil accueil = new Accueil(new java.awt.Frame(), true);
       accueil.setVisible(true);
        }
  }catch(Exception e){
      JOptionPane.showMessageDialog(this, "Le nom d'utilisateur ou le mot de passe est erron√© ");
      
  }
  
    }//GEN-LAST:event_jButton1ActionPerformed

